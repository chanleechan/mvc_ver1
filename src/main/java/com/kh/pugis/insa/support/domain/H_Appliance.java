//일반가전 정보성클래스
public class H_Appliance {
	String h_app_code;
	String h_app_name;
	int h_price;
	int h_amount;
	public String getH_app_code() {
		return h_app_code;
	}
	public void setH_app_code(String h_app_code) {
		this.h_app_code = h_app_code;
	}
	public String getH_app_name() {
		return h_app_name;
	}
	public void setH_app_name(String h_app_name) {
		this.h_app_name = h_app_name;
	}
	public int getH_price() {
		return h_price;
	}
	public void setH_price(int h_price) {
		this.h_price = h_price;
	}
	public int getH_amount() {
		return h_amount;
	}
	public void setH_amount(int h_amount) {
		this.h_amount = h_amount;
	}
	
}
